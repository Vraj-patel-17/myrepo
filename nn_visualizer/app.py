import tkinter as tk
root=tk.Tk()
root.geometry("500x400")
canvas=tk.Canvas(root,bg="#757575",height=380,width=450)
def draw_neuron(x,y,radius):

    canvas.create_oval(x-radius,y-radius,x+radius,y+radius,fill="white")
draw_neuron(100,220,20)
draw_neuron(100,120,20)
def connect_neuron(x1,y1,x2,y2):
    canvas.create_line(x1,y1,x2,y2,fill="black")
connect_neuron(100,200,100,140)
draw_neuron(200,70,20)
draw_neuron(200,180,20)
draw_neuron(200,290,20)
draw_neuron(320,180,20)
connect_neuron(120,120,180,70)
connect_neuron(120,120,180,180)
connect_neuron(120,220,180,290)
connect_neuron(120,120,180,290)
connect_neuron(120,220,180,70)
connect_neuron(120,220,180,180)
connect_neuron(200,90,200,160)
connect_neuron(200,200,200,270)
connect_neuron(220,70,300,180)
connect_neuron(220,180,300,180)
connect_neuron(220,290,300,180)
canvas.pack()
root.mainloop()